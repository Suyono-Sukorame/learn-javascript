let kalimat = "Hari ini adalah hari yang cerah";

// Mengakses karakter individual dalam string menggunakan indeks
console.log(kalimat[0]); // Output: H (karakter pada indeks ke-0)
console.log(kalimat[5]); // Output: i (karakter pada indeks ke-5)
console.log(kalimat[15]); // Output: h (karakter pada indeks ke-15)

// Mendapatkan panjang string menggunakan properti length
console.log(kalimat.length); // Output: 31 (jumlah karakter dalam string "Hari ini adalah hari yang cerah")
