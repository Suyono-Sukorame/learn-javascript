// Contoh penggunaan dalam struktur kendali
let usia = 18;
let sudahDewasa = usia >= 18; // Hasil evaluasi akan berupa true atau false

if (sudahDewasa) {
  console.log("Anda sudah dewasa");
} else {
  console.log("Anda masih di bawah umur");
}
// Output (bergantung pada nilai variabel usia): "Anda sudah dewasa" atau "Anda masih di bawah umur"
