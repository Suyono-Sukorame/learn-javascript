// String
let nama = "John Doe";
console.log(typeof nama); // Output: string

// Number
let umur = 25;
console.log(typeof umur); // Output: number

// Boolean
let isActive = true;
console.log(typeof isActive); // Output: boolean

// Null
let dataNull = null;
console.log(typeof dataNull); // Output: object (a known bug in JavaScript)

// Undefined
let dataUndefined;
console.log(typeof dataUndefined); // Output: undefined

// Symbol
const simbol = Symbol("deskripsi");
console.log(typeof simbol); // Output: symbol
