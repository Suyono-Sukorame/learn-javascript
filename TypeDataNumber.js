// Deklarasi variabel dengan tipe data number
let angka1 = 10;
let angka2 = 3.14;
let angkaNegatif = -7;

// Operasi matematika menggunakan tipe data number
let hasilPenjumlahan = angka1 + angka2;
let hasilPengurangan = angka1 - angka2;
let hasilPerkalian = angka1 * angka2;
let hasilPembagian = angka1 / angka2;
let hasilModulus = angka1 % angka2; // Sisa pembagian

// Output hasil operasi matematika
console.log("Hasil penjumlahan:", hasilPenjumlahan);
console.log("Hasil pengurangan:", hasilPengurangan);
console.log("Hasil perkalian:", hasilPerkalian);
console.log("Hasil pembagian:", hasilPembagian);
console.log("Sisa pembagian (modulus):", hasilModulus);

